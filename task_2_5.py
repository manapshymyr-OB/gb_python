"""5. Создать список, содержащий цены на товары (10–20 товаров), например:
[57.8, 46.51, 97, ...]

Вывести на экран эти цены через запятую в одну строку, цена должна отображаться в виде <r> руб <kk> коп (например «5 руб 04 коп»). Подумать, как из цены получить рубли и копейки, как добавить нули, если, например, получилось 7 копеек или 0 копеек (должно быть 07 коп или 00 коп).
Вывести цены, отсортированные по возрастанию, новый список не создавать (доказать, что объект списка после сортировки остался тот же).
Создать новый список, содержащий те же цены, но отсортированные по убыванию.
Вывести цены пяти самых дорогих товаров. Сможете ли вывести цены этих товаров по возрастанию, написав минимум кода?"""

prices = [57.8, 100, 46.51, 77.89, 77, 38.71, 38.98, 39, 45, 12.13]
print(len(prices))

prices_str = ''
for price in prices:

    prices_split = str(price).split('.')
    rub = prices_split[0]

    if len(prices_split) == 2:
        kopeek = prices_split[1]
        kopeek = kopeek.ljust(2, '0')
    else:
        kopeek = '00'

    prices_str += '"' + rub + ' руб ' + kopeek + ' коп", '

prices_str = prices_str.strip()[:-1]
print(prices_str)

"""Вывести цены, отсортированные по возрастанию, новый список не создавать (доказать, что объект списка после сортировки остался тот же).
"""


print("Object id before sorting: ", id(prices))

print("Prices before sorting: ", prices)
prices.sort(reverse=False)
print("Object id after sorting: ", id(prices))
print("Prices after sorting: ", prices)

"""Создать новый список, содержащий те же цены, но отсортированные по убыванию.
"""
desc_lst = sorted(prices, reverse=True)
print(desc_lst)

"""Вывести цены пяти самых дорогих товаров. Сможете ли вывести цены этих товаров по возрастанию, написав минимум кода?"""
print("Top 5: ", desc_lst[:5])